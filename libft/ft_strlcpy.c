#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	x;
	size_t	len;

	i = 0;
	x = size - 1;
	if (size > 0)
	{
		while (i < x && src[i] != '\0')
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	while (src[i] != '\0')
	{
		i++;
	}
	len = i;
	return (len);
}
